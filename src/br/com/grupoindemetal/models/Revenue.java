package br.com.grupoindemetal.models;

public class Revenue {
	
	private String month;
	private float total;
	
	// Getters and setters
	public String getMonth() {
		return month;
	}
	
	public void setMonth(String month) {
		this.month = month;
	}
	
	public float getTotal() {
		return total;
	}
	
	public void setTotal(float total) {
		this.total = total;
	}
	
}
